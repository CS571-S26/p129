const routePaths = {
  "Lake Monona Loop": lakeMononaPath,
  "Lake Wingra Loop": lakeWingraPath,
  "UW Arboretum Trails": uwArboretumPath
};

function DiscoverRoutesPage({ user }) {
  const [routes, setRoutes] = useState([]);
  const [loading, setLoading] = useState(true);
  const [selectedRoute, setSelectedRoute] = useState(null);
  const [selectedMarker, setSelectedMarker] = useState(null);
  const [map, setMap] = useState(null);
  const [favorites, setFavorites] = useState([]);
  const [showAlert, setShowAlert] = useState(false);
  const [alertMessage, setAlertMessage] = useState('');
  const [mapLoaded, setMapLoaded] = useState(false);
  const mapRef = useRef(null);

  useEffect(() => {
    fetchRoutes();
    if (user) {
      fetchFavorites();
    }
  }, [user]);

  useEffect(() => {
    setMapLoaded(false);
  }, []);

  const fetchRoutes = async () => {
    try {
      const querySnapshot = await getDocs(collection(db, "routes"));
      const routesData = [];
      querySnapshot.forEach((doc) => {
        const route = { id: doc.id, ...doc.data() };
        route.path = routePaths[route.name] || [];
        routesData.push(route);
      });
      setRoutes(routesData);
      setLoading(false);
    } catch (error) {
      console.error("Error fetching routes:", error);
      setLoading(false);
    }
  };

  const fetchFavorites = async () => {
    try {
      const q = query(collection(db, "favorites"), where("userId", "==", user.email));
      const querySnapshot = await getDocsQuery(q);
      const favoritesData = [];
      querySnapshot.forEach((doc) => {
        favoritesData.push(doc.data().routeId);
      });
      setFavorites(favoritesData);
    } catch (error) {
      console.error("Error fetching favorites:", error);
    }
  };

  const addToFavorites = async (route) => {
    try {
      await addDoc(collection(db, "favorites"), {
        userId: user.email,
        routeId: route.id,
        routeName: route.name,
        addedAt: new Date().toISOString()
      });
      setFavorites([...favorites, route.id]);
      setAlertMessage(`${route.name} added to favorites! ❤️`);
      setShowAlert(true);
      setTimeout(() => setShowAlert(false), 3000);
    } catch (error) {
      console.error("Error adding to favorites:", error);
    }
  };

  const removeFromFavorites = async (routeId, routeName) => {
    try {
      const q = query(collection(db, "favorites"), 
        where("userId", "==", user.email), 
        where("routeId", "==", routeId)
      );
      const querySnapshot = await getDocsQuery(q);
      querySnapshot.forEach(async (doc) => {
        await deleteDoc(doc.ref);
      });
      setFavorites(favorites.filter(id => id !== routeId));
      setAlertMessage(`${routeName} removed from favorites`);
      setShowAlert(true);
      setTimeout(() => setShowAlert(false), 3000);
    } catch (error) {
      console.error("Error removing from favorites:", error);
    }
  };

  const isFavorite = (routeId) => {
    return favorites.includes(routeId);
  };

  const onLoad = useCallback((map) => {
    setMap(map);
    setMapLoaded(true);
    mapRef.current = map;
  }, []);

  const onUnmount = useCallback(() => {
    setMap(null);
    setMapLoaded(false);
    mapRef.current = null;
  }, []);

  const handleRouteSelect = (route) => {
    setSelectedRoute(route);
    setSelectedMarker({ lat: route.startLat, lng: route.startLng });
    if (mapRef.current) {
      mapRef.current.panTo({ lat: route.startLat, lng: route.startLng });
      mapRef.current.setZoom(14);
    }
  };

  const getDifficultyBadge = (difficulty) => {
    switch(difficulty) {
      case 'Easy':
        return <Badge bg="success" className="difficulty-badge">Easy</Badge>;
      case 'Moderate':
        return <Badge bg="warning" className="difficulty-badge">Moderate</Badge>;
      case 'Hard':
        return <Badge bg="danger" className="difficulty-badge">Hard</Badge>;
      default:
        return <Badge bg="secondary">{difficulty}</Badge>;
    }
  };

  const apiKey = process.env.REACT_APP_GOOGLE_MAPS_API_KEY;

  if (!apiKey) {
    return (
      <Container className="mt-5">
        <Alert variant="danger">
          <Alert.Heading>Google Maps API Key Missing</Alert.Heading>
          <p>Please add your Google Maps API key to the .env file</p>
        </Alert>
      </Container>
    );
  }

  if (loading) {
    return (
      <div className="loading-container">
        <Spinner animation="border" variant="danger" />
        <p>Loading routes...</p>
      </div>
    );
  }

  return (
    <div className="discover-routes-page">
      {showAlert && (
        <div className="success-alert">
          {alertMessage}
        </div>
      )}

      <div className="routes-hero-section">
        <Container>
          <Row>
            <Col>
              <h1 className="routes-hero-title">Discover Running Routes 🏃‍♀️</h1>
              <p className="routes-hero-subtitle">
                Explore the best running paths in Madison, Wisconsin
              </p>
            </Col>
          </Row>
        </Container>
      </div>

      <Container fluid className="mt-4 mb-5">
        <Row>
          <Col lg={5} xl={4} className="routes-list-container">
            <div className="routes-list-header">
              <h3>All Routes</h3>
              <p>{routes.length} routes available</p>
            </div>
            <div className="routes-list">
              {routes.map(route => (
                <Card 
                  key={route.id} 
                  className={`route-card ${selectedRoute?.id === route.id ? 'active' : ''}`}
                  onClick={() => handleRouteSelect(route)}
                >
                  <Card.Body>
                    <div className="route-card-header">
                      <Card.Title>
                        {route.name}
                        {route.popular && <Badge bg="danger" className="ms-2">Popular</Badge>}
                        {isFavorite(route.id) && <Badge bg="info" className="ms-2">❤️ Favorite</Badge>}
                      </Card.Title>
                      {getDifficultyBadge(route.difficulty)}
                    </div>
                    <div className="route-stats">
                      <span>📏 {route.distance}</span>
                      <span>⛰️ {route.elevation}</span>
                      <span>⏱️ {route.estimatedTime}</span>
                    </div>
                    <div className="route-terrain">
                      <small>🛤️ {route.terrain}</small>
                    </div>
                  </Card.Body>
                </Card>
              ))}
            </div>
          </Col>

          <Col lg={7} xl={8}>
            <div className="map-container">
              <LoadScript 
                googleMapsApiKey={apiKey}
                loading="async"
              >
                <GoogleMap
                  mapContainerStyle={mapContainerStyle}
                  center={center}
                  zoom={12}
                  options={mapOptions}
                  onLoad={onLoad}
                  onUnmount={onUnmount}
                >
                  {routes.map(route => route.path && route.path.length > 0 && (
                    <Polyline
                      key={`path-${route.id}`}
                      path={route.path}
                      options={{
                        strokeColor: selectedRoute?.id === route.id ? '#FF6B6B' : '#4ECDC4',
                        strokeOpacity: 0.8,
                        strokeWeight: selectedRoute?.id === route.id ? 5 : 3,
                      }}
                    />
                  ))}

                  {routes.map(route => (
                    <Marker
                      key={`marker-${route.id}`}
                      position={{ lat: route.startLat, lng: route.startLng }}
                      onClick={() => {
                        setSelectedMarker({ lat: route.startLat, lng: route.startLng });
                        handleRouteSelect(route);
                      }}
                      icon={{
                        url: isFavorite(route.id) 
                          ? 'https://maps.google.com/mapfiles/ms/icons/purple-dot.png'
                          : 'https://maps.google.com/mapfiles/ms/icons/red-dot.png',
                        scaledSize: window.google && new window.google.maps.Size(32, 32)
                      }}
                    />
                  ))}

                  {selectedRoute && selectedMarker && window.google && (
                    <InfoWindow
                      position={selectedMarker}
                      onCloseClick={() => setSelectedMarker(null)}
                    >
                      <div className="info-window">
                        <h6>{selectedRoute.name}</h6>
                        <p>{selectedRoute.distance} • {selectedRoute.difficulty}</p>
                        <Button 
                          variant="danger" 
                          size="sm"
                          onClick={() => handleRouteSelect(selectedRoute)}
                        >
                          View Details
                        </Button>
                      </div>
                    </InfoWindow>
                  )}
                </GoogleMap>
              </LoadScript>
              {!mapLoaded && (
                <div className="map-loading-overlay">
                  <Spinner animation="border" variant="danger" />
                  <p>Loading map...</p>
                </div>
              )}
            </div>

            {selectedRoute ? (
              <div className="route-details-panel">
                <div className="route-details-header">
                  <h3>{selectedRoute.name}</h3>
                  {getDifficultyBadge(selectedRoute.difficulty)}
                </div>
                
                <Row className="route-details-stats">
                  <Col md={3} sm={6}>
                    <div className="detail-stat">
                      <div className="stat-icon">📏</div>
                      <div className="stat-label">Distance</div>
                      <div className="stat-value">{selectedRoute.distance}</div>
                    </div>
                  </Col>
                  <Col md={3} sm={6}>
                    <div className="detail-stat">
                      <div className="stat-icon">⛰️</div>
                      <div className="stat-label">Elevation</div>
                      <div className="stat-value">{selectedRoute.elevation}</div>
                    </div>
                  </Col>
                  <Col md={3} sm={6}>
                    <div className="detail-stat">
                      <div className="stat-icon">🛤️</div>
                      <div className="stat-label">Terrain</div>
                      <div className="stat-value">{selectedRoute.terrain}</div>
                    </div>
                  </Col>
                  <Col md={3} sm={6}>
                    <div className="detail-stat">
                      <div className="stat-icon">⏱️</div>
                      <div className="stat-label">Est. Time</div>
                      <div className="stat-value">{selectedRoute.estimatedTime}</div>
                    </div>
                  </Col>
                </Row>

                <div className="route-description">
                  <h5>About this route</h5>
                  <p>{selectedRoute.description}</p>
                </div>

                <div className="route-actions">
                  {isFavorite(selectedRoute.id) ? (
                    <Button 
                      variant="outline-danger" 
                      size="lg" 
                      onClick={() => removeFromFavorites(selectedRoute.id, selectedRoute.name)}
                    >
                      ❤️ Remove from Favorites
                    </Button>
                  ) : (
                    <Button 
                      variant="danger" 
                      size="lg" 
                      onClick={() => addToFavorites(selectedRoute)}
                    >
                      🤍 Add to Favorites
                    </Button>
                  )}
                </div>
              </div>
            ) : (
              <div className="no-route-selected">
                <div className="no-route-content">
                  <span className="no-route-icon">🗺️</span>
                  <h4>Select a route to see details</h4>
                  <p>Click on any route from the list or tap a marker on the map</p>
                </div>
              </div>
            )}
          </Col>
        </Row>
      </Container>
    </div>
  );
}

export default DiscoverRoutesPage;